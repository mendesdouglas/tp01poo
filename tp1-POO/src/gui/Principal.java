	private void botaoLoginOkActionPerformed(ActionEvent evt) {//GEN-FIRST:event_botaoLoginOkActionPerformed
		if (AccessControl.login(painelLoginNomeUsuario.getText(),new String(painelLoginSenhaUsuario.getPassword()),true)){
		menuPrincipalCadastros.setEnabled(true);
		menuPrincipalConsultas.setEnabled(true);
		menuPrincipalMovimento.setEnabled(true);
		painelLogin.setVisible(false);
		JOptionPane.showConfirmDialog(null,"Bem Vindo "+painelLoginNomeUsuario.getText(),"mensagem",JOptionPane.CLOSED_OPTION);    
		}
		else{
			JOptionPane.showConfirmDialog(null,"Nome de usuário ou senha estão incorretos","mensagem",JOptionPane.CLOSED_OPTION);
			painelLoginNomeUsuario.setText("");
			painelLoginSenhaUsuario.setText("");
		}
	}//GEN-LAST:event_botaoLoginOkActionPerformed

