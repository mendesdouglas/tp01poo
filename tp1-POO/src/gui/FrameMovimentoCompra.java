	private void cadastrar() {
		Integer codigo = 0,quant = 0;
		Float preco = (float)0.0;
		String nome;
		Item item = null;
		
		if(compraTextFieldCodigo.getText() == null || compraTextFieldCodigo.getText().length() == 0){
			mensagem("O campo Código não pode estar em branco");
			return;
		}
		else{
			try{
				codigo = Integer.parseInt(compraTextFieldCodigo.getText());
				item = PersistenciaEstoque.getInstance().searchItem(codigo);
			}
			catch (NumberFormatException e) {
				nome = compraTextFieldCodigo.getText();
				item = PersistenciaEstoque.getInstance().searchItem(nome);
			}
		}
		if(compraTextFieldPreco.getText() == null || compraTextFieldPreco.getText().length() == 0){
			mensagem("O campo Preço não pode estar em branco");
			return;
		}
		else{
			try{
				preco = Float.parseFloat(compraTextFieldPreco.getText());
				if(preco <= 0 ){
					mensagem("Preço deve ser maior que zero");
					return;
				}
			}
			catch (NumberFormatException e) {
				mensagem("O campo Preço não é um valor válido");
				return;
			}
		}
		if(compraTextFieldQuantidade.getText() == null || compraTextFieldQuantidade.getText().length() == 0){
			mensagem("O campo Quantidade não pode estar em branco");
			return;
		}
		else{
			try{
				quant = Integer.parseInt(compraTextFieldQuantidade.getText());
				if(quant <= 0 ){
					mensagem("Quantidade deve ser maior que zero");
					return;
				}
			}
			catch (NumberFormatException e) {
				mensagem("O campo quantidade não é um valor válido");
				return;
			}
		}
		if (item == null ){
			mensagem("Item não encontrado");
			return;
		}
		else{
			if(compra.addItem(item, preco, quant)){
				jTable3.setModel(getData());
				limpaCampos();
			}
			else{
				mensagem("oops!nao adicionei");
			}
		}
	}
	
	private TableModel getData(){
		ArrayList<ItemCompra> itens = compra.overview();
		Vector<Vector<String>> dados = new Vector<Vector<String>>();
		Vector<String> headers = new Vector<String>();
		String [] headersName = new String[]{"linha","Código","Nome","Preço", "Quantidade", "subTotal"};
		int linha=0;
		Float total = (float) 0.0,subtotal = (float) 0.0;
		for (int i = 0; i < headersName.length; i++) {
			headers.add(headersName[i]);
		}
		if (itens != null){
			for (ItemCompra itensC : itens) {
		    	subtotal = itensC.getPrecoCompra() * itensC.getQuant();
				Vector<String> vetor = new Vector<String>();
				vetor.add(((Integer)linha).toString());
				vetor.add(((Integer)itensC.getCodigoItem()).toString());
				vetor.add(itensC.getNomeItem());
				vetor.add(((Float)itensC.getPrecoCompra()).toString());
				vetor.add(((Integer)itensC.getQuant()).toString());
				vetor.add(subtotal.toString());
				total+=subtotal;
				dados.add(linha,vetor);
				linha++;
			}
		}
		else{
			DefaultTableModel model = new DefaultTableModel(new String[][]{}, headersName);
			labelPreco.setText("0.00");
			labelQuantidade.setText("0");
			return model;			
		}
		DefaultTableModel model = new DefaultTableModel(dados, headers);
		labelPreco.setText(total.toString());
		labelQuantidade.setText(((Integer)dados.size()).toString());
		return model;
 	}

