	private void adicionar() {
		Integer codigo = 0,quant = 0;
		String nome;
		Item item = null;
		
		if(campoCodigo.getText() == null || campoCodigo.getText().length() == 0){
			mensagem("O campo Código não pode estar em branco");
			return;
		}
		else{
			try{
				codigo = Integer.parseInt(campoCodigo.getText());
				item = PersistenciaEstoque.getInstance().searchItem(codigo);
			}
			catch (NumberFormatException e) {
				nome = campoCodigo.getText();
				item = PersistenciaEstoque.getInstance().searchItem(nome);
			}
		}

		if(campoQuantidade.getText() == null || campoQuantidade.getText().length() == 0){
			mensagem("O campo Quantidade não pode estar em branco");
			return;
		}
		else{
			try{
				quant = Integer.parseInt(campoQuantidade.getText());
				if(quant <= 0 ){
					mensagem("Quantidade deve ser maior que zero");
					return;
				}
			}
			catch (NumberFormatException e) {
				mensagem("O campo quantidade não é um valor válido");
				return;
			}
		}
		if (item == null ){
			mensagem("Item não encontrado");
			return;
		}
		else{
			if(pedido.addItem(item,quant)){
				jTable1.setModel(getData());
				limpaCampos();
			}
			else{
				mensagem("oops!nao adicionei");
			}
		}
	}
	
	private TableModel getData(){
		ArrayList<ItemPedido> itens = pedido.overview();
		Vector<Vector<String>> dados = new Vector<Vector<String>>();
		Vector<String> headers = new Vector<String>();
		String [] headersName = new String[]{"Linha","Código","Nome","Preco","Quant","Subtotal"};
		int linha=0;
		Float total = (float) 0.0,subtotal = (float) 0.0;
		for (int i = 0; i < headersName.length; i++) {
			headers.add(headersName[i]);
		}
		if (itens != null){
			for (ItemPedido itensP : itens) {
		    	subtotal = itensP.getPrecoPedido() * itensP.getQuant();
				Vector<String> vetor = new Vector<String>();
				vetor.add(((Integer)linha).toString());
				vetor.add(((Integer)itensP.getCodigoItem()).toString());
				vetor.add(itensP.getNomeItem());
				vetor.add(((Float)itensP.getPrecoPedido()).toString());
				vetor.add(((Integer)itensP.getQuant()).toString());
				vetor.add(subtotal.toString());
				total+=subtotal;
				dados.add(linha,vetor);
				linha++;
			}
		}
		else{
			DefaultTableModel model = new DefaultTableModel(new String[][]{}, headersName);
			labelPreco.setText("0.00");
			labelQuantidade.setText("0");
			return model;			
		}
		DefaultTableModel model = new DefaultTableModel(dados, headers);
		labelPreco.setText(total.toString());
		labelQuantidade.setText(((Integer)dados.size()).toString());
		return model;
 	}

