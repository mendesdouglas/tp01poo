	private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
		int selecionado = jTable1.getSelectedRow();
		if(selecionado >= 0) {
			pedido.delItem(selecionado);
			jTable1.setModel(getData());
			}
		else{
			mensagem("nenhuma linha selecionada");
			return;
		}
	}//GEN-LAST:event_botaoRemoverActionPerformed

	private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed
		int selecionado = jTable1.getSelectedRow();
		if(selecionado >= 0) {
			campoCodigo.setText(((Integer)pedido.overview().get(selecionado).getCodigoItem()).toString());
			campoCodigo.setEditable(false);
			campoQuantidade.setText(((Integer)pedido.overview().get(selecionado).getQuant()).toString());
			pedido.delItem(selecionado);
			jTable1.setModel(getData());
			}
		else{
			mensagem("nenhuma linha selecionada");
			return;
		}
	}//GEN-LAST:event_botaoEditarActionPerformed

